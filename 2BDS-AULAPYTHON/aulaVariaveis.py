#NUMERO INTEIRO - INT - Representam números inteiros, positivos ou negativos, sem casas decimais
numInteiro=1000

#NUMERO DECIMAL - FLOAR - Representam números com casas decimais
numDecimal=999.99

#TEXTO SIMPLES OU COMPOSTO - STRING
#Representam cadeias de caracteres, ou seja, texto
textostring="Bem Vindo a Linguagem Python "

#VALOR LÓGICO - BOOLEAN -
#Representam valores lógicos, podendo ser True ou False
valorLogico= True

#LISTA - LIST - Coleções ordenadas e mutáveis de elementos
#Podem armazernar diferentes tipos de dados
listaDeNumeros = [1, 2, 3, 4]

#TULAS - TUPLE - Semelhantes às listas, mas imutáveis
tuplaDeNumeros = ( 10, 20)

print(numInteiro)
print(numDecimal)
print(textostring)
print(valorLogico)
print(listaDeNumeros)
print(tuplaDeNumeros)

print(type(numInteiro))
print(type(numDecimal))
print(type(textostring))
print(type(valorLogico))
print(type(listaDeNumeros))
print(type(tuplaDeNumeros))
